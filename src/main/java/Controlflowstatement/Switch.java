package Controlflowstatement;

public class Switch {

	public static void main(String args[]) {
	int num = 20;
	switch(num) {
	case 10:
	System.out.print("10");
	break;
	case 20:
	System.out.print("20");
	break;
	case 30:
	System.out.print("30");
	break;
	default:
	System.out.print("This is default case");
	}
		
	}
	
}
